CREATE OR REPLACE FORCE VIEW TRAMITE.V_DOCUMENTO AS
SELECT
     D.ID_DOCUMENTO,
     D.NRO_CUT,
     D.ID_TIPO,
     CASE WHEN D.ID_TIPO = 1 THEN
         'DIGITAL'
     ELSE
         'FISICO'
     END  DESC_TIPO,
     D.ID_TIPO_DOCUMENTO,
     TD.DESC_TIPO_DOCUMENTO,
     D.ASUNTO,
     D.REFERENCIA,
     D.ID_ESTADO_DOCUMENTO,
     ED.DESC_ESTADO_DOCUMENTO,
     '' DESTINOS,
     TO_CHAR(D.CORRELATIVO_DOCUMENTO) CORRELATIVO_DOCUMENTO,
     D.NRO_DOCUMENTO NUMERO_DOCUMENTO,
     TO_CHAR(D.FEC_FIRMA, 'DD/MM/YYYY hh:mi:ss a.m.') FECHA_FIRMA,
     D.CLAVE_FIRMA,
     D.FLG_ESTADO,
     D.ID_USUARIO,
     D.ID_OFICINA,
     D.USU_CREACION,
     D.FEC_CREACION,
     NVL(TO_CHAR(D.FEC_CREACION, 'DD/MM/YYYY hh:mi:ss a.m.'), '-') STR_FEC_CREACION,
     NVL(D.IP_CREACION, '-') IP_CREACION,
     NVL(D.USU_MODIFICACION, '-') USU_MODIFICACION,
     D.FEC_MODIFICACION,
     NVL(TO_CHAR(D.FEC_MODIFICACION, 'DD/MM/YYYY hh:mi:ss a.m.'), '-') STR_FEC_MODIFICACION,
     NVL(D.IP_MODIFICACION, '-') IP_MODIFICACION
 FROM
     TRA_M_DOCUMENTO D
     INNER JOIN TRA_L_ESTADO_DOCUMENTO ED ON ED.ID_ESTADO_DOCUMENTO = D.ID_ESTADO_DOCUMENTO
     INNER JOIN V_TIPO_DOCUMENTO TD ON TD.ID_TIPO_DOCUMENTO = D.ID_TIPO_DOCUMENTO;

