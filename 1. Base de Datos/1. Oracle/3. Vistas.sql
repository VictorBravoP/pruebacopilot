CREATE OR REPLACE FORCE VIEW TRAMITE.V_TIPO_DOCUMENTO AS
SELECT
      TD.ID_TIPO_DOCUMENTO,
      TD.DESC_TIPO_DOCUMENTO,
      TD.SIGLA_TIPO_DOCUMENTO,
